package com.atex.gong.data.publish.page;

public class DuplicatePage {
    private String sourcePageId;
    private String targetParentId;
    private String aceId;
    private String title;
    private String description;

    public String getSourcePageId() { return sourcePageId; }
    public void setSourcePageId(String v) { this.sourcePageId = v; }
    public String getTargetParentId() { return targetParentId; }
    public void setTargetParentId(String v) { this.targetParentId = v; }
    public String getAceId() { return aceId; }
    public void setAceId(String v) { this.aceId = v; }
    public String getTitle() { return title; }
    public void setTitle(String v) { this.title = v; }
    public String getDescription() { return description; }
    public void setDescription(String v) { this.description = v; }
}

