package com.atex.onecms.app.dam.standard.aspects;

public class DamAudioAspectBean extends OneContentBean {
    private String audioLink;
    private String duration;
    private String description;

    public String getAudioLink() { return audioLink; }
    public void setAudioLink(String v) { this.audioLink = v; }
    public String getDuration() { return duration; }
    public void setDuration(String v) { this.duration = v; }
    public String getDescription() { return description; }
    public void setDescription(String v) { this.description = v; }
}
