package com.atex.onecms.app.dam.types;

public class SocialPostingAccount {

    private String platform;
    private String id;
    private Long postTime;

    public String getPlatform() { return platform; }
    public void setPlatform(final String platform) { this.platform = platform; }
    public String getId() { return id; }
    public void setId(final String id) { this.id = id; }
    public Long getPostTime() { return postTime; }
    public void setPostTime(final Long postTime) { this.postTime = postTime; }
}

